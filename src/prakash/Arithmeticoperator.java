package prakash;

public class Arithmeticoperator 
{

	public static void main(String[] args) 
	{
     int a=20,b=10;
     System.out.println(a);
     System.out.println(b);
     System.out.println(a+b);
     System.out.println(a-b);
     System.out.println(a*b);
     System.out.println(a/b);
     System.out.println(a%b);
     
	}

}
