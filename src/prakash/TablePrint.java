package prakash;

public class TablePrint {

	public static void main(String[] args) {


		for(int num=2;num<=10;num++)
		{
			System.out.println("===================");
			System.out.println("Table for :"+num);
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(num+"X"+i+"="+num*i);
		}

	}
	}

}
